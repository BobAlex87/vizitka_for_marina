async def get_start(message: Message, bot: Bot):
    """
    –ü—Ä–∏–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ, –æ—Ç–≤–µ—Ç –Ω–∞ –∫–æ–º–∞–Ω–¥—É /start
    :param message: Message
    :param bot: Bot
    :return: –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç –∏ –¥–≤–µ –∏–Ω–ª–∞–π–Ω-–∫–Ω–æ–ø–∫–∏:"–±–æ–ª—å—à–µ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏"(–æ—Ç–∫—Ä—ã–≤–∞–µ—Ç —Å–ª–µ–¥—É—é—â–∏–π –±–ª–æ–∫ –∫–Ω–æ–ø–æ–∫) –∏
    "–ø—Ä–∏—Å–æ–µ–¥–∏–Ω–∏—Ç—å—Å—è" (–ø–µ—Ä–µ–∞–¥—Ä–µ—Å–æ–≤—ã–≤–∞–µ—Ç –Ω–∞ —Ä–µ—Ñ–µ—Ä–∞–ª—å–Ω—É—é —Å—Å—ã–ª–∫—É)
    """
    await db_start(message, bot)
    photo = FSInputFile("core/images/LiveGoog.jpg")
    await message.answer_photo(photo)
    await message.answer(f'–ü—Ä–∏–≤–µ—Ç {message.from_user.first_name}üëã —Ä–∞–¥ —Ç–µ–±—è –≤–∏–¥–µ—Ç—å!'
                         f'\n–°–µ–π—á–∞—Å —è —Ç–µ–±—è –ø–æ–∑–Ω–∞–∫–æ–º–ª—é —Å –Ω–æ–≤—ã–º –ø—Ä–æ–µ–∫—Ç–æ–º <b>LiveGood</b>'
                         f'\n–≤ –∫–æ—Ç–æ—Ä–æ–º –º–æ–∂–Ω–æ –∑–∞—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –ø—Ä–∏ –º–∏–Ω–∏–º–∞–ª—å–Ω—ã—Ö –≤–ª–æ–∂–µ–Ω–∏—è—Ö'
                         f'\n–∏ —Ä–∞—Å—Å–∫–∞–∂—É –∫–∞–∫ –ø–æ—Å—Ç—Ä–æ–∏—Ç—å –ø–∞—Å—Å–∏–≤–Ω—ã–π –¥–æ—Ö–æ–¥ –¥–æ <b>2000 –≥—Ä–Ω/–º–µ—Å—è—Ü</b>',
                         reply_markup=first_inline_keyboard)


